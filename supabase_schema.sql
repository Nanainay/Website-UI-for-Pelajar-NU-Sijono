-- 11. SYNC AUTO-INCREMENT SEQUENCES SAFELY
DO $$
DECLARE
    t RECORD;
    seq TEXT;
BEGIN
    FOR t IN 
        SELECT table_name 
        FROM information_schema.tables 
        WHERE table_schema = 'public' 
          AND table_name IN ('roles', 'articles', 'articles_old', 'service_requests', 'surat_masuk', 'surat_keluar', 'letter_requests', 'letter_documents')
    LOOP
        seq := pg_get_serial_sequence('public.' || t.table_name, 'id');
        IF seq IS NOT NULL THEN
            EXECUTE format('SELECT setval(%L, COALESCE((SELECT MAX(id) FROM %I), 1))', seq, t.table_name);
        END IF;
    END LOOP;
END $$;

