-- 8. DEFINE TRIGGERS AND FUNCTIONS
-- AUTO PROFILE SYNC FUNCTION
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.users (id, name, email, role_id, nia, phone)
  VALUES (
    new.id,
    COALESCE(new.raw_user_meta_data->>'name', 'Anggota Baru'),
    new.email,
    COALESCE((new.raw_user_meta_data->>'role_id')::BIGINT, 4),
    new.raw_user_meta_data->>'nia',
    new.raw_user_meta_data->>'phone'
  )
  ON CONFLICT (id) DO UPDATE
  SET name = EXCLUDED.name, 
      email = EXCLUDED.email,
      role_id = EXCLUDED.role_id,
      nia = EXCLUDED.nia,
      phone = EXCLUDED.phone;
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- DROP OLD TRIGGER IF EXISTS (Prevents stale reference after schema reset)
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;

-- REGISTER PROFILE SYNC TRIGGER
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- AUTO UPDATED_AT TIMESTAMP FUNCTION
CREATE OR REPLACE FUNCTION trigger_set_timestamp()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- REGISTER TIMESTAMP TRIGGER
